def split_list(source: List, row_length: int) -> List[List]:
    """
    Renvoi une List en 2d à partir d'une origine et d'une longueur de ligne.
    Chaque ligne (row) du tableau sera de la longueur `row_length`.
    """
    assert row_length > 0, "Le paramètre `row_length` doit être un nombre entier positif non null"
    assert type(source) == list and len(source) > 0, "Le paramètre `source` doit être le tableau non vide à séparer"
    # contient les données de la ligne sur le point d'être ajoutée
    temp = []
    # Contient la liste 2D
    array = []
    # On parcourt chaque élément
    for elm in source:
        # On ajoute l'élément
        temp.append(elm)
        # Si `temp` a la taille requise, on l'ajoute à `array` et on nettoie `temp`
        if len(temp) >= row_length:
            array.append(temp)
            temp = []
    # On n'oublie rien
    if len(temp) > 0:
        array.append(temp)
    return array


